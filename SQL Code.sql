CREATE TABLE ADMINS(
	admin_id INT IDENTITY(1,1) PRIMARY KEY,
	first_name VARCHAR(15) NOT NULL,
	last_name VARCHAR(15) NOT NULL,
	email VARCHAR(25) NOT NULL UNIQUE,
	password VARCHAR(25) NOT NULL
);

CREATE TABLE CLIENTS(
	client_id INT IDENTITY(1,1) PRIMARY KEY,
	first_name VARCHAR(15) NOT NULL,
	last_name VARCHAR(15) NOT NULL,
	id_number VARCHAR(9) NOT NULL UNIQUE,
	unique_id_number VARCHAR(13) NOT NULL UNIQUE,
	passport_number VARCHAR(9) NOT NULL UNIQUE,
	phone_number VARCHAR(10) NOT NULL UNIQUE,
	address VARCHAR(30) NOT NULL,
	email VARCHAR(25) NOT NULL,
	password VARCHAR(25) NOT NULL
);

CREATE TABLE LOCATIONS(
	location_id INT IDENTITY(1,1) PRIMARY KEY,
	location_name VARCHAR(15) NOT NULL UNIQUE,
);

CREATE TABLE ARRANGEMENTS(
	arrangement_id INT IDENTITY(1,1) PRIMARY KEY,
	name VARCHAR(30) NOT NULL,
	date_of_departure DATE NOT NULL,
	return_date DATE NOT NULL,
	location_id INT NOT NULL,
	type_of_transport VARCHAR(30),
	type_of_arrangement VARCHAR(30),
	number_of_vacancies int NOT NULL,
	description VARCHAR(1000),
	price decimal NOT NULL,
	FOREIGN KEY(location_id) REFERENCES LOCATIONS(location_id)
);

CREATE TABLE CLIENTS_ARRANGEMENTS(
	arrangement_id INT,
	client_id INT,
	number_of_people INT NOT NULL,
	PRIMARY KEY(arrangement_id,client_id),
	FOREIGN KEY (arrangement_id) REFERENCES ARRANGEMENTS(arrangement_id),
	FOREIGN KEY (client_id) REFERENCES CLIENTS(client_id)
);

CREATE TABLE TICKETS(
	ticket_id INT IDENTITY(1,1) PRIMARY KEY,
	name VARCHAR(30) NOT NULL,
	date_of_departure DATE NOT NULL,
	return_date DATE NOT NULL,
	location_id INT NOT NULL,
	type_of_transport VARCHAR(30),
	price decimal NOT NULL,
	number_of_vacancies int NOT NULL,
	FOREIGN KEY(location_id) REFERENCES LOCATIONS(location_id)
);

CREATE TABLE CLIENTS_TICKETS(
	ticket_id INT,
	client_id INT,
	number_of_people INT NOT NULL,
	PRIMARY KEY(ticket_id,client_id),
	FOREIGN KEY (ticket_id) REFERENCES TICKETS(ticket_id),
	FOREIGN KEY (client_id) REFERENCES CLIENTS(client_id)
);


CREATE TABLE EXCHANGE_RATES(
	currency_id VARCHAR(3) PRIMARY KEY,
	currency_code VARCHAR(3) NOT NULL UNIQUE,
	country_name VARCHAR(30) NOT NULL,
	mean_exchange_rate decimal NOT NULL
);


